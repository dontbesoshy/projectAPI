<?php

namespace App\Resources\MainPhoto\BO;

use App\Resources\BasicResource;

class MainPhotoResource extends BasicResource
{
    /**
     * @param $request
     *
     * @return array
     */
    public function toArray($request): array
    {
        return [
            'id' => $this->id,
            'name' => $this->name,
            'url' => $this->url,
        ];
    }
}
